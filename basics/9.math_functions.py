import math

pi = 3.14

# rounding a number
print(round(pi))
print(math.ceil(pi))

# rounding down
print(math.floor(pi))

# absolute value
print(abs(-pi))

# to power of 
print(pow(pi, 2)) 

# square root
print(math.sqrt(pi))

# find the largest number
x = 1
y = 2
z = 3

print(max(x, y, z)) # 3


# find the smallest number
print(min(x, y, z)) # 1