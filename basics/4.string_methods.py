name = "Andy"

# 1. getting length of the string (4)
print(len(name))

# 2. finding an index of element (3~y)
print(name.find("y"))

# 3. Capitalize first letter in string  
print(name.capitalize())

# 4. uppercase the string 
print(name.upper())

# 5. lowercase the string
print(name.lower())

# 6. isdigit checks if it contains digits (1,2,3...)
print(name.isdigit())

# 7. isalpha checks if it contains only alphabet letter
print(name.isalpha())

# 8. get number of substrings inside of the string
print(name.count("a"))

# 9. replace a substring inside a string 
print(name.replace("y", "rew"))

# 10. multiply the str (andyandyandyandy)
print(name * 4)